<h3>РАСЧЕТЫ В ФОРМЕ БАНКОВСКИХ ПЕРЕВОДОВ</h3>
<p>Банковский перевод представляет собой поручение одного лица (перевододателя) банку перевести определенную сумму валюты в пользу другого лица (переводополучателя). При внешнеторговой деятельности банковские переводы используются прежде всего для выполнения своих обязательств по договорам купли-продажи товаров и услуг, погашения кредитов, переводов неторгового характера.</p>

<h1>Экспорт</h1>
<p>Банк осуществляет переводы через свои корреспондентские счета, открытые в соответствующих валютах в иностранных банках.</p>
<p>При экспорте российское предприятие открывает в банке счет в той валюте, в которой ожидается поступление экспортной выручки. Внешнеторговый контракт в этом случае должен содержать номер этого валютного счета, наименование переводополучателя (бенефициара) , а так же наименование банка-корреспондента и номер корреспондентского счета, через который будет производится перевод.</p>

<h1>Импорт</h1>
<p>При расчетах по импорту предприятие представляет в банк платежное поручение на перечисление валюты с валютного счета на счет своего контрагента-продавца. Инструкции по переводу указываются импортером в соответствии с внешнеторговым контрактом и содержат:</p>
<ul class="list2">
	<li>номер и дату поручения;</li>
	<li>полное наименование и адрес перевододателя;</li>
	<li>валюта и сумма перевода цифрами и прописью;</li>
	<li>наименование и адрес банка бенефициара, его идентификационный номер (SWIFT, BLZ, FED и т.д.)  или корреспондентский счет;</li>
	<li>наименование и адрес банка посредника, его идентификационный номер;</li>
	<li>наименование и адрес бенефициара, номер его банковского счета;</li>
	<li>детали платежа: номера и даты контрактов, счетов;</li>
	<li>кто оплачивает комиссии банка по переводу;</li>
	<li>номер счета, с которого происходит списание суммы платежа.</li>
</ul>
<p>Платежное поручение должно быть подписано и заверено печатью.</p>
<p>Наименования и адреса бенефициара и банков дается без сокращений и перевода на русский язык.</p>
<p>В случае, если расходы перевододатель относит за свой счет, банк предложит за исполнение платежного поручения уплатить сумму, определенную тарифами. Если расходы относятся за счет переводополучателя, банк дебетует валютный счет перевододателя на сумму платежа, удерживает из нее свои комиссии, и переводит оставшуюся сумму переводополучателю.Во избежание разногласий оговаривайте в договорах купли-продажи, кто несет расходы по исполнению перевода.</p>
<p>Не используйте при заполнении платежных поручений реквизиты, указанные на бланках писем Ваших партнеров или продиктованные по телефону. Часто бланки содержат устаревшую информацию или сведения о счетах в иных валютах, чем валюта платежа.</p>
<p>Консультацию по правильному заполнению платежного поручения Вы можете получить в управлении международных операций банка.</p>
<p>Сумма в иностранной валюте может быть переведена с текущего валютного счета импортера, если она получена им в результате внешнеэкономиской деятельности или в виде кредита. Если импортер не располагает необходимой для перевода суммой валюты, то он может купить ее за рубли через банк.</p>

