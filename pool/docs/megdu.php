<h3>АККРЕДИТИВЫ В МЕЖДУНАРОДНОЙ ТОРГОВЛЕ</h3>

<p>При заключении внешнеторговой сделки операция купли-продажи значительно усложняется по сравнению с операциями внутри страны в силу ряда причин: времени, затрачиваемого на перевозку; рисков, связанных с транспортировкой; таможеных формальностей; экспортно-импортного регулирования и валютного контроля в разных странах. Кроме этого стороны могут не иметь опыта торговли друг с другом и, следовательно, не иметь уверенности в том, что покупатель совершит платеж полностью и в срок, а продавец поставит товары вовремя, нужного качества и количества.</p>
<p>Это приводит к необходимости применения такой формы расчетов, которая в максимальной степени защитила бы интересы как продавца так и покупателя. Покупатель должен быть уверен, что он заплатил за нужный ему товар, а продавец- что его товары будут оплачены. Для того, чтобы удовлетворить эти противоречивые интересы и минимизировать риски широко используется документарный аккредитив.</p>
<p>Документарный аккредитив представляет собой обязательство банка выплатить бенефициару (продавцу) оговоренную сумму при условии представления в банк в установленные сроки документов, свидетельствующих об осуществлении в срок отгрузки определенных товаров.</p>
<p>Решение об осуществлении такого платежа банк принимает только на основании представленных документов и не связан с условиями существующего договора купли-продажи и особенностями его исполнения сторонами. Таким образом, интересы покупателя соблюдаются представлением документов, свидетельствующих об исполнении договора : транспортные документы свидетельствуют о произведенной отгрузке, страховые полисы- об осуществленном страховании, сертификаты качества (количества, веса) - о соответствии товара условиям договора и т.д.</p>
<p>Международная торговая палата разработала "Унифицированные правила и обычаи для документарных аккредитивов" - общепризнанный стандарт работы для всех сторон, участвующих в аккредитивной сделке. Подписывая международный договор купли-продажи, предусматривающий аккредитивную форму расчетов, следует внимательно изучить эти правила для того, чтобы правильно оценивать степень риска при использовании аккредитива. В настоящее время действует редакция Правил 2007 года (публикация МТП N 600).</p>
<p>Приступая к работе над аккредитивом проверьте, располагаете ли Вы следующей информацией:</p>
<ul class="list2">
	<li>наименование аппликанта (приказодателя) и его адрес;</li>
	<li>наименование бенефициара и его адрес;</li>
	<li>требуется ли подтверждение аккредитива;</li>
	<li>каков должен быть срок действия аккредитива;</li>
	<li>валюта и сумма аккредитива;</li>
	<li>наименование и адрес авизующего и исполняющего банков;</li>
	<li>допускает ли договор купли-продажи частичные отгрузки и перевалку товара;</li>
	<li>каким способом должен исполняться аккредитив (путем платежа, платежа с рассрочкой, акцепта или негоциации);</li>
	<li>место отгрузки, место доставки товара и условия поставки;</li>
	<li>наименование товара;</li>
	<li>требуемые документы (должны соответствовать условиям поставки и выбранному способу транспортировки);</li>
	<li>последняя дата отгрузки;</li>
	<li>срок для представления документов в исполняющий банк;</li>
	<li>кто оплачивает банковские комиссии по аккредитиву.</li>
</ul>
<p>Если в договоре купли-продажи эта информация отсутствует, то открытый аккредитив может не устроить бенефициара, и это приведет к необходимости внесения  изменений. Помните, что нарушение хотя бы одного из условий аккредитива или Унифицированных правил может привести к отказу банка оплатить документы.</p>
<p>Документы, которые часто встречаются при аккредитивной форме расчетов:</p>
<ul class="list2">
	<li>переводной вексель (тратта);</li>
	<li>счет-фактура (инвойс);</li>
	<li>сертификат происхождения товара;</li>
	<li>сертификат качества;</li>
	<li>упаковочный лист (весовой лист);</li>
	<li>страховой полис;</li>
	<li>транспортные документы:</li>
	<ol class="list2">
		<li>коносамент (перевозка морем);</li>
		<li>накладная (автомобильный/железнодорожный транспорт);</li>
		<li>авианакладная (воздушный транспорт);</li>
		<li>комбинированный транспортный коносамент (несколько видов транспорта);</li>
		<li>и т.д.</li>
	</ol>
</ul>

<h1>Импорт</h1>
<p>После подписания договора купли-продажи покупатель должен обратиться в банк с поручением на открытие аккредитива в пользу продавца. Вам предложат заполнить бланк заявления на открытие акредитива. Открыв аккредитив, банк сообщает его условия бенефициару. Обычно это делается через банк продавца (авизующий банк). Выполнив условия договора купли-продажи, бенефициар представляет необходимые документы в исполняющий банк и получает от него оплату. После чего документы передаются аппликанту (покупателю).</p>

<h1>Экспорт</h1>
<p>Аккредитив открывается иностранным банком (банком покупателя) в пользу продавца. Условия аккредитива Ваш банк передаст Вам в том случае, если он будет назван авизующим в договоре купли-продажи. Внимательно ознакомтесь с текстом и определите, не содержит ли аккредитив условий, неприемлемых или невыполнимых для Вас. В случае необходимости просите покупателя изменить аккредитив. После того как отгрузка будет осуществлена, оформите документы и представьте их в свой банк. Если документы не содержат расхождений с  условиями аккредитива и представлены вовремя, то исполняющий банк произведет оплату на Ваш валютный счет.</p>
