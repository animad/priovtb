<p>Прио-Внешторгбанк (ОАО) -  оказывает услуги по реализации процесса выдачи заработной платы с использованием пластиковых карт.
<p>Начисление и выплата заработной платы с помощью пластиковых карт позволяет предприятию уменьшить расходы по движению средств в наличной форме, сократить объем кассовых операций бухгалтерии, избавиться от очереди за зарплатой, снизить риски хранения и транспортировки наличных, предназначенных к выплате сотрудникам. Для целей выплаты зарплаты служат карты, созданной банком локальной платежной системы "Прио-карт".</p>
<p>Применение "Прио-карт" - удобный, доступный, современный способ получения заработной платы, который оценили по достоинству тысячи рязанцев. Используя "Прио-карт" сотрудники предприятия получают новый высококачественный и удобный сервис, обладающий перед прочими рядом преимуществ. Это бесплатный выпуск карты, а в случае ее утраты бесплатный перевыпуск; отсутствие платы за годовое обслуживание;  самостоятельный ввод персонального идентификационного номера, а при необходимости его бесплатное изменение; постоянное наличие денежных средств в банкоматах и кассах банка.  Выдача наличных денежных средств осуществляется как в городе Рязани, так и в филиалах банка в Рязанской области в удобное для . Для удобства обслуживания клиентов большинство операционных касс банка работает до восьми часов вечера с одним выходным днем, а ряд операционных касс работает без выходных дней.</p>
<p>Прио-Внешторгбанк (ОАО) планирует в начале 2004 года расширить сеть пунктов выдачи наличных в городе, а также предоставить владельцам "Прио-карт" возможность получения наличных в Московском филиале.</p>
