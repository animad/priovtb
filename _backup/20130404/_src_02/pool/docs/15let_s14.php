<p><b>I. ОБЩИЕ ПОЛОЖЕНИЯ </b>
<p><b>1.1. Цели конкурса.</b>
<ul type=disc title="" compact>
<LI>привлечение печатных и электронных средств массовой информации к более широкому и объективному освещению деятельности Банка и региональной банковской системы; 
<LI>повышение степени информированности жителей Рязанской области о деятельности Банка и банков - участников региональной банковской системы; 
<LI>совершенствование форм взаимодействия Банка со средствами массовой информации; 
<LI>стимулирование творческой деятельности журналистов, пишущих на актуальные темы; 
<LI>активная поддержка журналистов и изданий, публикующих материалы о повседневной деятельности Банка, систематически освещающих проблемы развития банковского сообщества.
</ul>
<p><b>1.2. Организаторы конкурса.</b>
<p>Конкурс организует и проводит Совет директоров и Правление Прио-Внешторгбанка. 
<p><b>1.3. Участники.</b>
<p>В конкурсе принимают участие редакции областных, районных и городских печатных и электронных средств массовой информации Рязанской области независимо от их организационно-правовой формы, журналисты независимо от стажа работы и возраста чьи материалы, посвященные деятельности Банка или региональной банковской системе в целом, были опубликованы в СМИ в период с 1 января 2005 года по 1 декабря 2005 года. Количество выдвигаемых материалов, созданных одним автором не ограничивается. Одна и та же конкурсная работа не может быть представлена в несколько номинаций. 

<p><b>1.4. Выдвижение материалов на конкурс.</b>
<p>
Право выдвижения участников конкурса предоставляется: 
<ul type=disc title="" compact><LI> коллективам редакций печатных и электронных средств массовой информации; 
<LI> пресс-службой Банка, коллективами районных филиалов и отделений Банка; 
<LI> Ассоциации рязанских банков "Рязанский банкирский дом"; 
<LI> отдельным журналистам посредством самовыдвижения. </ul>
К участию в конкурсе не принимаются материалы, опубликованные на правах рекламы.
<p><b>1.5. Сроки</b>
<p>
Работы будут приниматься до 1 декабря 2005 года. Награждение победителей состоится в рамках празднования 16-й годовщины со дня образования Банка не позднее 20 декабря 2005 года. 

