Положение о проведении конкурса среди печатных и электронных средств массовой информации Рязанской области на лучшее освещение событий происходящих в региональной банковской системе, посвященного 15-летию Прио-Внешторгбанка
6 декабря 2004 года исполнилось 15 лет со дня основания Прио-Внешторгбанка. В рамках проведения праздничных мероприятий приуроченных к этой годовщине Совет директоров и Правление Прио-Внешторгбанка объявляет о проведении конкурса на лучшие журналистские работы, освещающие деятельность Прио-Внешторгбанка и региональной банковской системы в целом. 
