<p>На 1 января 2007 года активы банка в соответствии с публикуемой отчетностью составили 3 981 млн. руб., увеличившись за год на 972 млн. руб. (32%).</p>
<p>Источники собственных средств выросли за год с 266,3 млн. руб. до 427,8 млн. руб. Рост собственных ресурсов обеспечен за счет полученной банком прибыли и проведения переоценки стоимости принадлежащих банку объектов недвижимости.</p>
<p>Чистая прибыль за 2006 год с учетом СПОД  составила   63,6 млн. руб., рентабельность капитала  (отношение полученной за год прибыли к капиталу на начало отчетного периода) – 23,9%,  рентабельность активов (прибыль, отнесенная к сумме активов на начало отчетного периода)- 2,1%.</p>
<p>Основным источником доходов банка являются кредитные операции. Чистая ссудная и приравненная к ней задолженность (включая депозиты в Банке России) составила на 1.01.2007 г. 3 150 млн. руб., то есть выросла за год на 35%.</p>
<p>Вложения в ценные бумаги, акции и участие составили 221 млн. руб. (рост за год на 146 млн. руб.).</p>
<p>Рост объемов активных операций обеспечивается ростом ресурсной базы. Средства клиентов (некредитных организаций) составили 3 415 млн. руб. и выросли за год на 32%, при этом вклады граждан выросли на 27 %, депозиты юридических лиц – на 42 %, остатки на расчетных счетах – на   39 %.</p>
<p>Размер уставного фонда в капитале банка составил 35 млн. руб., резервного фонда – 5,25 млн. руб., или 15% к оплаченному капиталу, что полностью соответствует требованиям Банка России и Устава банка.</p>
<p>Отчисления банка в фонд обязательных резервов в сумме 55,5 млн. руб. соответствуют нормативным требованиям Банка России.</p>
<p>Банк является участником системы страхования вкладов. Сумма перечисленных в адрес ГК «Агентство по страхованию вкладов» страховых взносов составила 18,3 млн. руб.</p>

