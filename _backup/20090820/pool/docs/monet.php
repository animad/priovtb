<p>Начиная с 70-х годов в нашей стране осуществляются выпуски монет из драгоценных металлов коллекционного и инвестиционного назначения. В советский период все они предназначались для реализации на международном рынке, а на территории СССР продавались только иностранцам за свободно конвертируемую валюту. Лишь в 90-х годах, по мере отмены ряда законодательных и административных запретов и ограничений, эти монеты начинают реализовываться в России, за российские рубли - возник и развивается отечественный рынок монет.</p>
<p>В конце 90-х годов Банк России ежегодно выпускает в обращение до четырех десятков серебряных и несколько видов золотых монет (до 1995 года выпускались также монеты из платины и палладия), причем более 50% их тиража распространяется на отечественном рынке. Сегодня на нем можно встретить памятные и инвестиционные монеты из золота, серебра, платины и палладия с датами выпуска, начиная с 1975 года.</p>
