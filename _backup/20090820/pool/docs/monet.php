<p>По особенностям технологического процесса чеканки монеты делятся на две основные группы:</p>
<br />
<ul class="list2">
	<li>исполненные в качестве "пруф" (от англ. <b>"proof"</b>);</li>
	<li>исполненные в обычном (англ. <b>"uncirculated"</b>) или улучшенном (англ. <b>"brilliant uncirculated"</b>) качестве.</li>
</ul>
<p>Монеты качества "пруф" изготавливаются методом, позволяющим получить на поверхности монеты чистое, зеркальное поле и матовое рельефное изображение и надписи. Это монеты, как правило, коллекционного назначения, их выпуск либо приурочивается к юбилеям и памятным датам, знаменательным событиям истории и современности, либо посвящается актуальным проблемам культуры, защиты окружающей среды, другим аналогичным темам. Рисунки их реверсов зачастую сложные по композиции, многоплановые; надписи указывают на тематику, которой посвящен выпуск монет.</p>
<p>Монеты обычного качества - это продукция традиционного высокоавтоматизированного чеканного производства: курсовые и разменные монеты (обращающиеся по номиналам в качестве реальных платежных средств), а также монеты из драгоценных металлов инвестиционного назначения, тиражи которых могут достигать нескольких миллионов штук. На монетах этого вида нет зеркальных поверхностей, поля, рисунки и надписи имеют одинаковую поверхностную фактуру, рельефы не контрастируют с полями. Рисунки на реверсах таких монет зачастую несложные, одноплановые.</p>
<p>Монеты улучшенного качества отличаются от монет обычного качества главным образом тем, что они не имеют мелких насечек, царапин и других повреждений, обусловленных особенностями автоматизированного производства, что достигается более тщательной подготовкой инструментов и заготовок, а также мерами по защите монет от механических воздействий при выбросе готовой продукции в накопители станков и других технологических участках автоматической подачи.</p>
<p>Банк России выпускает монеты обычного качества из драгоценных металлов. К таким монетам относятся "червонец" образца 1975-1982 годов, золотые монеты из серии "Знаки зодиака". Операции с неколлекционными монетами из драгоценных металлов не облагаются налогом на добавленную стоимость, в отличие от слиткового золота, коллекционных монет из драгоценных металлов и ювелирных изделий. Из-за особенностей налогообложения эти монеты являются удобным и выгодным способом помещения капитала в золото.</p>
