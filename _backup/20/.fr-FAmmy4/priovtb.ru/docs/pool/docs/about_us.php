<h1>About us</h1>
<p>Prio-Vneshtorgbank JSC was established on December 6, 1989 as Bank Prioksky. On December 27, 1991, it merged with Prombusinessbank and changed its name to Prio-Vneshtorgbank.</p>
<p>The bank possesses banking license ¹ 212 of Central Bank of the Russian Federation for execution of banking operations in rubles and foreign currency for legal entities and private persons, for attraction of deposits and disposition of precious metals, and other operations with precious metals in accordance with the legislation of the Russian Federation.</p>
<p>Prio-Vneshtorgbank JSC offers a wide range of products and services for corporate customers and private persons in Ryazan region and other regions. At present the bank has 8 operating cash-desks, 7 additional offices, and 1 branch in Moscow. Prio-Vneshtorgbank JSC is one of the most important  financial institutions among the independent Ryazan banks.</p>
<p>The bank is a member of Moscow Interbank Currency Exchange, REUTERS, SWIFT, the Association of Russian Banks, CONTACT System, and State Corporation “Deposit Insurance Agency”.</p>
